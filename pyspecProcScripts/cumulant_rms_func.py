import numpy as np


def cumulant_rms(d, indirect_dim, direct_dim="$B_0$"):
    r"""Return the normalized cumulative RMS change along an indirect axis.

    This diagnostic follows how much the ESR spectrum changes from one
    indirect point to the next.  Smooth alignment should reduce sharp
    horizontal-jitter artifacts, making this cumulant smoother along the
    indirect dimension.

    For an ESR spectrum :math:`S(B,t)`, where :math:`t` is the
    acquisition/order coordinate of the indirect dimension,

    .. math::

      C(t) =
      \int_{0}^{t}
       \frac{
          \sqrt{
             \int
             \left|
                \frac{\partial}{\partial t'} S(B,t')}
             \right|^2
             dB
          }
       }{
          \sqrt{
             \int |S(B,t')|^2 dB
          }
       }
       dt'

    The discrete implementation uses ``np.diff`` along ``indirect_dim``.
    Because the difference has one fewer indirect point, the denominator is
    placed on the same staggered grid by averaging adjacent spectrum RMS
    values.
    """
    # pyspecdata wraps np.diff with its same-length internal difference helper;
    # the final point is generated by that helper, so drop it explicitly.
    cumulant = (
        d.real.run(np.diff, indirect_dim)[indirect_dim, :-1]
        .run(lambda x: abs(x) ** 2)
        .mean(direct_dim)
        .run(np.sqrt)
    )
    spectrum_rms = (
        d.real.run(lambda x: abs(x) ** 2).mean(direct_dim).run(np.sqrt)
    )
    # Normalize by the energy of the spectrum at each step.
    # This way if e.g. it's just the amplitude of the spectrum changing,
    # we will see that.
    cumulant /= (
        spectrum_rms[indirect_dim, :-1]
        + spectrum_rms[indirect_dim, 1:]
    ) / 2
    # note that, deliberately, at this point, we just have normalized
    # differences only once we have that do we take the cumulant.
    cumulant.run(np.cumsum, indirect_dim)
    return cumulant
